								Jalan Malioboro (bahasa Jawa: Hanacaraka, ꦢꦭꦤ꧀​ꦩꦭꦶꦲꦧꦫ , Dalan Malioboro) adalah nama salah satu kawasan jalan dari tiga jalan di Kota Yogyakarta yang membentang dari Tugu Yogyakarta hingga ke perempatan Kantor Pos Yogyakarta. Secara keseluruhan terdiri dari Jalan Margo Utomo, Jalan Malioboro, dan Jalan Margo Mulyo. Jalan ini merupakan poros Garis Imajiner Kraton Yogyakarta.

